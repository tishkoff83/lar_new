                                <p>Актер Максим Виторган поделился впечатлениями от сериала режиссера Павла Костомарова <a href="#">"Эпидемия"</a>. Звезда призналась, что нашла в теленовелле "некоторые странности".</p>

                                <p><strong>Домашний карантин:</strong> Максим Виторган проводит в том числе за просмотром сериалов. По словам артиста, он за один присест ознакомился со всеми эпизодами теленовеллы "Эпидемия". </p>
                                </blockquote>

                                <p>Картина Виторгану очень понравилась. Особенно он хвалил работу Павла Костомарова.

                                    "Это так приятно видеть, что человек умеет делать свое дело. Умеет снимать кино. Все это выглядит дороже предусмотренных сметой денег. Апокалипсическая картина мира написана простым карандашом с поразительным мастерством. Прекрасные актерские работы. Все. А без режиссуры так не бывает. Эпидемия поражает людей. Даже если им удается уберечься от вируса", – написал Максим в личном "Инстаграм".</p>

                                <h2>Set a bigger goals and chase them everyday</h2>
                                <p>Между тем Виторган не стал скрывать, что "Эпидемия" получилась "не без недостатков". В последних двух сериях теленовеллы артист заметил "некоторые странности в разрешении образов". Первые эпизоды сделаны на "высочайшем уровне органики", поэтому на их фоне недочеты в финале становятся заметнее. "В общем, смотреть обязательно. Да и самое время сейчас",– посоветовала поклонникам знаменитость.


                                    Большинство подписчиков согласилось с мнением Максима Виторгана. Тем временем были и те, кому сериал "Эпидемия" не понравился.</p>

