                                <p>"Не хотелось бы никого обидеть, но я вот не разделяю зрительских восторгов. Слишком много суеты в сюжете, рваное повествование, как будто спешили передать атмосферу истерии. Персонажи немного карикатурные: хамоватый бизнесмен и его, конечно же, молодая жена, дочь – бунтарка, соседка - психолог с неустойчивой психикой, бывшая жена, обиженная на весь мир. Мне показалось, что слишком много стереотипов и в передаче характеров, и в передаче людских реакций на эпидемию, не хватило глубины переживаний", – написал пользователь Сети.</p>



